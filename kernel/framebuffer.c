/* Local copy of the boot info. */
static boot_info_t fb;
static int fb_ready = 0;

/* For brevity, treat the LFB as a flat byte array and write
 * either 2 (16bpp), 3 (24bpp), or 4 (32bpp) bytes per pixel. */
static inline uint8_t *fb_pixel_ptr(int x, int y) {
    return (uint8_t *)(uintptr_t)fb.lfb_phys + (uint32_t)y * fb.pitch + (uint32_t)x * (fb.bpp / 8);
}

static inline void put_pixel(int x, int y, uint32_t color) {
    if ((unsigned)x >= fb.width || (unsigned)y >= fb.height) return;
    uint8_t *p = fb_pixel_ptr(x, y);
    if (fb.bpp == 32) {
        *(uint32_t *)p = color;
    } else if (fb.bpp == 24) {
        p[0] = color & 0xFF;
        p[1] = (color >> 8) & 0xFF;
        p[2] = (color >> 16) & 0xFF;
    } else if (fb.bpp == 16) {
        /* 5-6-5 */
        uint16_t r = (color >> 16) & 0xFF;
        uint16_t g = (color >> 8) & 0xFF;
        uint16_t b = color & 0xFF;
        uint16_t v = ((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3);
        *(uint16_t *)p = v;
    }
}

static inline uint32_t get_pixel(int x, int y) {
    if ((unsigned)x >= fb.width || (unsigned)y >= fb.height) return 0;
    uint8_t *p = fb_pixel_ptr(x, y);
    if (fb.bpp == 32) {
        return *(uint32_t *)p;
    } else if (fb.bpp == 24) {
        return (uint32_t)p[0] | ((uint32_t)p[1] << 8) | ((uint32_t)p[2] << 16);
    } else if (fb.bpp == 16) {
        uint16_t v = *(uint16_t *)p;
        uint32_t r = ((v >> 11) & 0x1F) << 3;
        uint32_t g = ((v >> 5) & 0x3F) << 2;
        uint32_t b = (v & 0x1F) << 3;
        return (r << 16) | (g << 8) | b;
    }
    return 0;
}

int fb_init(void) {
    boot_info_t *src = (boot_info_t *)BOOT_INFO_ADDR;
    if (!src->mode_ok) return -1;
    fb = *src;
    if (fb.width == 0 || fb.height == 0 || fb.lfb_phys == 0) return -1;
    if (fb.bpp != 16 && fb.bpp != 24 && fb.bpp != 32) return -1;

    /* Identity-map the framebuffer into the kernel page directory. */
    uint32_t size = fb.pitch * fb.height;
    uint32_t base = fb.lfb_phys & 0xFFFFF000;
    uint32_t end  = (fb.lfb_phys + size + PAGE_SIZE - 1) & 0xFFFFF000;
    for (uint32_t a = base; a < end; a += PAGE_SIZE) {
        paging_map(a, a, PAGE_PRESENT | PAGE_WRITABLE);
    }

    fb_ready = 1;
    return 0;
}

int fb_is_ready(void) { return fb_ready; }
int fb_width(void)    { return fb_ready ? (int)fb.width  : 0; }
int fb_height(void)   { return fb_ready ? (int)fb.height : 0; }

void fb_pixel(int x, int y, uint32_t color) {
    if (!fb_ready) return;
    put_pixel(x, y, color);
}

void fb_clear(uint32_t color) {
    if (!fb_ready) return;
    fb_fill_rect(0, 0, fb.width, fb.height, color);
}

void fb_fill_rect(int x, int y, int w, int h, uint32_t color) {
    if (!fb_ready) return;
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (x + w > (int)fb.width)  w = fb.width  - x;
    if (y + h > (int)fb.height) h = fb.height - y;
    if (w <= 0 || h <= 0) return;

    if (fb.bpp == 32) {
        for (int row = 0; row < h; row++) {
            uint32_t *p = (uint32_t *)fb_pixel_ptr(x, y + row);
            for (int col = 0; col < w; col++) p[col] = color;
        }
    } else {
        for (int row = 0; row < h; row++) {
            for (int col = 0; col < w; col++) {
                put_pixel(x + col, y + row, color);
            }
        }
    }
}

void fb_rect(int x, int y, int w, int h, uint32_t color) {
    fb_hline(x, y, w, color);
    fb_hline(x, y + h - 1, w, color);
    fb_vline(x, y, h, color);
    fb_vline(x + w - 1, y, h, color);
}

void fb_hline(int x, int y, int w, uint32_t color) {
    fb_fill_rect(x, y, w, 1, color);
}

void fb_vline(int x, int y, int h, uint32_t color) {
    fb_fill_rect(x, y, 1, h, color);
}

void fb_save_block(int x, int y, int w, int h, uint32_t *out) {
    if (!fb_ready) return;
    for (int row = 0; row < h; row++) {
        for (int col = 0; col < w; col++) {
            out[row * w + col] = get_pixel(x + col, y + row);
        }
    }
}

void fb_restore_block(int x, int y, int w, int h, const uint32_t *in) {
    if (!fb_ready) return;
    for (int row = 0; row < h; row++) {
        for (int col = 0; col < w; col++) {
            put_pixel(x + col, y + row, in[row * w + col]);
        }
    }
}

void fb_draw_char(int x, int y, char c, uint32_t fg, int scale) {
    if (!fb_ready) return;
    unsigned idx;
    if ((unsigned char)c < 0x20 || (unsigned char)c > 0x7E) {
        idx = '?' - 0x20;
    } else {
        idx = (unsigned char)c - 0x20;
    }
    const uint8_t *glyph = font8x8[idx];
    if (scale < 1) scale = 1;

    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            if (bits & (0x80 >> col)) {
                fb_fill_rect(x + col * scale, y + row * scale, scale, scale, fg);
            }
        }
    }
}

void fb_draw_string(int x, int y, const char *s, uint32_t fg, int scale) {
    if (!fb_ready) return;
    int cx = x;
    while (*s) {
        fb_draw_char(cx, y, *s, fg, scale);
        cx += 8 * scale;
        s++;
    }
}

int fb_text_width(const char *s, int scale) {
    int n = 0;
    while (s[n]) n++;
    return n * 8 * scale;
}
